value_1=float(input("Enter first number: "))
op=input("Enter operator : ")
value_2=float(input("Enter second number: "))

if op=='+':
    print(value_1 + value_2)
elif op=='-':
    print(value_1 - value_2)
elif op=='/':
    print(value_1/value_2)
elif op=='*':
    print(value_1*value_2)
else:
    print("Invalid operator")











